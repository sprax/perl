sub sortByMirs($\@\%\%) {
    my ($sN, $rvN, $rhL1, $rhR1) = @_;
    my (%sL1c, %sR1c);
    # print "\nsortByMirs BEFORE rvN(@$rvN)\n";
    foreach $c (@$rvN) {
        $sL1c{$c} = join '', (grep /^$c/, (keys %$rhL1));
        $sR1c{$c} = join '', (grep /$c$/, (keys %$rhR1));
      # print "sL1c $c  ($sL1c{$c})\n";
      # print "sR1c $c  ($sR1c{$c})\n";
    }

    if (1) {
        # pre-compute all pairwise comparisons:  O(N^2) w/ N <= 23.
        # TODO make this evaluation lazy so byNumMatches calls a memoized func?
        local %matches;
        my ($nL, $nR);
        foreach $c (@$rvN) {
            foreach $d (@$rvN) {
                next if $c eq $d;
                $nL = ()=($sL1c{$d} =~ /$c/g); # num matches of $c in L mirs
                $nL = $nL < 2 ? $nL : $nL * 2;  # bonus for 2 or more L matches 
                $nR = ()=($sR1c{$d} =~ /$c/g); # num matches of $c in R mirs
                $nR = $nR < 2 ? $nR : $nR * 2;  # bonus for 2 or more R matches 
                $matches{$c.$d} = $nL + $nR;    # total matches
            }
        }
        sub byNumMatches { 
            # TODO memoized code would go here...
            # FIXME race condition from  m(a) < m(b) < m(c) < m(a) !!
            $matches{$a.$b} <=> $matches{$b.$a};
        }
        @$rvN = sort byNumMatches @$rvN;
    }
    elsif (0) {
        local %matchRest;
        my ($nL, $nR, $sn);
        foreach $c (@$rvN) {
            $sn =  $sN;
            $sn =~ s/$c//;     # rm $c from $sn
            if ($sn) {
                $nL = ()=($sL1c{$c} =~ /[$sn]/g); # count occurrences of other 
                $nR = ()=($sR1c{$c} =~ /[$sn]/g); # needed letters in miL1|R1
            } else {
                $nL = $nR = 0;
            }
            $nL = $nL < 2 ? $nL : $nL * 2;  # bonus for 2 or more L matches 
            $nR = $nR < 2 ? $nR : $nR * 2;  # bonus for 2 or more R matches 
            $matchRest{$c} = $nL + $nR;     # total matches
            print "matchRest( $c )==$matchRest{$c}\n" if($c eq 'k'||$c eq 'z');
        }
        sub byNumMatchRest { 
            $matchRest{$b} <=> $matchRest{$a};
        }
        @$rvN = sort byNumMatchRest @$rvN;
    }
    else {
        local %rawLen;
        my ($zL, $zR);
        foreach $c (@$rvN) {
            $zL = length($sL1c{$c} || '');
            $zR = length($sR1c{$c} || '');
            $zL = $zL < 5 ? $zL : $zL * 2;
            $zR = $zR < 5 ? $zR : $zR * 2;
            $rawLen{$c} = $zL + $zR;
            print "rawLen( $c ) == $rawLen{$c}\n" if $c eq 'k';
        }
        sub byLength {
            $rawLen{$b} <=> $rawLen{$a};
        }
        @$rvN = sort byLength @$rvN;
    }
    # print "sortByMirs AFTER  rvN(@$rvN)\n";
    # return $rvN     # useful only if prototype were (\%\%\%@), but it ain't
}

