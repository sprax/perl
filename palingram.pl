# recursively divide a string into array of words, else return nil 
sub s2wordsOld($) {
    local $_ = shift @_;
    return ($_, @_) if $wrds{$_};
    my $z    = length($_);
    my $zBeg = $z - $maxWordLen > 0 ? $z - $maxWordLen : 0;
    $z      -= $minWordLen;
    while ($z >= $zBeg) {
        if ($wrds{my $ss = substr($_, $z)}) {
            my @wds = &s2wordsOld(substr($_,0,$z), $ss, @_);
            return @wds if @wds;
        }
        $z--;
    }
    return ();
}

# This version, which eliminates the call $z = length($_), runs only about 2%
# faster in simple benchmark test, which parsed 67-letters strings 100,000
# times (one was a PP, the other a PP except for one non-word in the middle).
sub s2words($$) {
    local  $z = shift @_;
    local  $_ = shift @_;
    return($_, @_) if $wrds{$_};
    my $zBeg = $z - $maxWordLen > 0 ? $z - $maxWordLen : 0;
    $z      -= $minWordLen;
    while ($z >= $zBeg) {       # biased toward smaller words...
        if ($wrds{my $ss = substr($_, $z)}) {
            if (my @wds = &s2words($z, substr($_,0,$z), $ss, @_)) {
                return @wds;
            }
        }
        $z--;
    }
    return ();  # return empty list
}


  
# recursively divide a string into a minimal array of words 
sub s2wordsMin($) {
    local $_ = shift @_;
#   print "s2wMin: ($_)(@_)\n";
    return ($_, @_) if $wrds{$_};

    my @min = ();
    my $z    = length($_);
    for (my $k = $minWordLen; $k < $z; $k++) {
        if ($wrds{my $ss = substr($_, $k)}) {
            @wds = &s2wordsMin(substr($_,0,$k), $ss, @_);
#         # print "b4:(@min) > (@wds)? ",scalar(@min)," > ",scalar(@wds),"?\n";
            if (@wds) {
#             # print "ws:(@min)>(@wds) ? ",scalar(@min),">",scalar(@wds),"\n";
                if (! @min || @min > @wds) {
                    @min = @wds;
                }
            }
        }
    }
#   print "min(@min)\n";
    return @min;
}

# recursively divide a string into a maximal array of words 
sub s2wordsMax($) {
    local $_ = shift @_;
    my $z    = length($_);
    print "s2wMax: $z ($_)(@_)\n";
    my $t    = $z - $maxWordLen;
    my $zBeg = $t > 0 ? $t : 0;
    my (@max, @wds);
    for ( $z -= $minWordLen; $z >= $zBeg; $z--) {
        if ($wrds{my $ss = substr($_, $z)}) {
            if ($z > 0) {
                @wds = &s2wordsMax(substr($_,0,$z), $ss, @_);
            } else {
                @wds = ($ss, @_);
            }
            if (@max < @wds) {
                @max = @wds;
            }
        }
    }
    print "max(@max)\n";
    return @max;
}

