sub letter_ords {
    my $vowel_cnt     = 0;
    my $letter_cnt    = 0;
    my $consonant_cnt = 0;
    foreach (sort {$letter_freqs{$b}<=>$letter_freqs{$a}} keys %letter_freqs) {
        # print "$_  $letter_freqs{$_}\n";
        push @letters_descend, $_;
        $letter_cnt += $letter_freqs{$_};
        if ($vowels{$_}) {
            push @vowels_descend, $_;
            $vowel_cnt  += $letter_freqs{$_}
        }
        else {
            push @consonants_descend, $_;
            $consonant_cnt  += $letter_freqs{$_}
        }
    }
    @letters_ascend = reverse @letters_descend;
    @letter_order   = reverse @consonants_descend; # rarest consonants first
    push  @letter_order,  reverse @vowels_descend; # then commonest vowels
    print "letter_order: (@letter_order)\n";
    print "letters_asc:  (@letters_ascend)\n";
}
